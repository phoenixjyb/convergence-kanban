let currentLang = localStorage.getItem('kanban-lang') || 'en';

function t(key) {
  return I18N[currentLang]?.[key] || I18N.en[key] || key;
}

function tField(obj, fieldBase) {
  let val;
  if (currentLang === 'zh') {
    const zh = obj[fieldBase + '_zh'];
    if (zh && zh.trim()) val = zh;
  }
  val = val || obj[fieldBase + '_en'] || obj[fieldBase] || '';
  return esc(val);
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'zh' : 'en';
  localStorage.setItem('kanban-lang', currentLang);
  document.getElementById('langBtn').textContent = currentLang === 'en' ? '中文' : 'EN';
  refreshDashboard();
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
}

function showToast(message, type = 'info', duration = 4000) {
  let container = document.getElementById('toasts');
  if (!container) {
    container = document.createElement('div');
    container.id = 'toasts';
    container.className = 'toast-container';
    document.body.appendChild(container);
  }
  const el = document.createElement('div');
  el.className = 'toast toast-' + type;
  el.textContent = message;
  container.appendChild(el);
  setTimeout(() => {
    el.classList.add('toast-out');
    setTimeout(() => el.remove(), 300);
  }, duration);
}
